/**
 * Calculate the distance metric between two colors
 *
 * @param a 
 * @param b 
 */
function distance(a: Color, b: Color) {
  const dr = a.r - b.r;
  const dg = a.g - b.g;
  const db = a.b - b.b;
  return Math.sqrt(dr * dr + dg * dg + db * db);
}

/**
 * Find the (approximate) name of any color
 *
 * @param rgb
 * @returns 
 */
function colorName(rgb: Color): string {
  const names = Object.keys(dictionary);
  names.sort((a, b) => {
    const da = distance(dictionary[a], rgb);
    const db = distance(dictionary[b], rgb);
    if (da < db) {
      return -1;
    } else {
      return 1;
    }
  });
  return names[0] ?? 'Unknown';
}

